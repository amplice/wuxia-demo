import { spawn } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { writeFileSync } from 'node:fs';
import { setTimeout as delay } from 'node:timers/promises';
import puppeteer from 'puppeteer';

const APP_PORT = Number(process.env.APP_PORT || 4274);
const APP_URL = `http://127.0.0.1:${APP_PORT}`;
const PROJECT_ROOT = fileURLToPath(new URL('../', import.meta.url));
const OUTPUT_PATH = fileURLToPath(new URL('../src/data/authoritativeTracks.js', import.meta.url));

function spawnProcess(command, args) {
  const isWinNpm = process.platform === 'win32' && command === 'npm';
  const resolvedCommand = isWinNpm ? 'cmd.exe' : command;
  const resolvedArgs = isWinNpm ? ['/c', 'npm.cmd', ...args] : args;
  const proc = spawn(resolvedCommand, resolvedArgs, {
    cwd: PROJECT_ROOT,
    env: { ...process.env },
    stdio: ['ignore', 'pipe', 'pipe'],
    shell: false,
  });
  let stderr = '';
  proc.stderr.on('data', (chunk) => {
    stderr += String(chunk);
  });
  return { proc, getStderr: () => stderr };
}

function killProcessTree(proc) {
  if (!proc || proc.killed) return;
  if (process.platform === 'win32') {
    const killer = spawn('taskkill', ['/pid', String(proc.pid), '/t', '/f'], {
      stdio: 'ignore',
      shell: false,
    });
    killer.unref();
    return;
  }
  proc.kill('SIGTERM');
}

async function waitForHttp(url, timeoutMs = 15000) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    try {
      const res = await fetch(url);
      if (res.ok) return true;
    } catch {
      // Server still starting.
    }
    await delay(200);
  }
  throw new Error(`Timed out waiting for ${url}`);
}

function asJsModule(data) {
  return `// Generated by scripts/generate-authoritative-tracks.mjs\nexport const AUTHORITATIVE_TRACKS = ${JSON.stringify(data, null, 2)};\n`;
}

async function main() {
  const app = spawnProcess('npm', ['run', 'dev', '--', '--host', '127.0.0.1', '--port', String(APP_PORT)]);
  let browser;

  try {
    await waitForHttp(APP_URL, 30000);
    browser = await puppeteer.launch({
      headless: true,
      protocolTimeout: 240000,
    });
    const page = await browser.newPage();
    await page.goto(APP_URL, { waitUntil: 'domcontentloaded' });

    const data = await page.evaluate(async () => {
      const waitForReady = async () => {
        const start = performance.now();
        while (performance.now() - start < 30000) {
          const game = window.__ringOfSteelGame;
          if (game?._charCache?.ronin && game?._charCache?.spearman) {
            return game;
          }
          await new Promise((resolve) => window.setTimeout(resolve, 100));
        }
        throw new Error('Game cache did not finish loading.');
      };

      const game = await waitForReady();
      const { ModelLoader } = await import('/src/entities/ModelLoader.js');

      const sampleClips = [
        'idle',
        'attack_quick',
        'attack_heavy',
        'attack_thrust',
      ];

      const round = (n) => Number(n.toFixed(6));
      const readWorldOffset = (node, root) => {
        const matrix = node.matrixWorld.elements;
        return [
          round(matrix[12] - root.position.x),
          round(matrix[13] - root.position.y),
          round(matrix[14] - root.position.z),
        ];
      };

      const result = {
        generatedAt: new Date().toISOString(),
        clips: sampleClips,
        characters: {},
      };

      for (const [charId, animData] of Object.entries(game._charCache)) {
        const fighter = ModelLoader.createFighterFromGLB(animData.model, animData.clips, animData.texture);
        const { root, joints, mixer, actions } = fighter;
        const bodyAnchorOffset = joints.bodyAnchor
          ? readWorldOffset(joints.bodyAnchor, root)
          : null;
        const baseTarget = joints.weaponBase || joints.handR || joints.handL;
        const tipTarget = joints.weaponTip || joints.spearTip;

        if (!baseTarget || !tipTarget) {
          throw new Error(`Missing weapon joints for ${charId}`);
        }

        const entry = {
          bodyAnchorOffset,
          clips: {},
        };

        for (const clipName of sampleClips) {
          const action = actions[clipName];
          if (!action) continue;

          const clip = action.getClip();
          const timeScale = Math.max(action.timeScale || 1, 1e-6);
          const duration = clip.duration / timeScale;
          const frameCount = Math.max(1, Math.ceil(duration * 60));
          const frames = [];

          action.reset();
          action.enabled = true;
          action.play();
          action.paused = true;

          for (let frame = 0; frame < frameCount; frame++) {
            const time = Math.min(frame / 60, Math.max(duration - (1 / 6000), 0));
            action.time = time * timeScale;
            mixer.update(0);
            root.updateWorldMatrix(true, true);
            frames.push({
              frame,
              time: round(time),
              base: readWorldOffset(baseTarget, root),
              tip: readWorldOffset(tipTarget, root),
            });
          }

          action.stop();
          action.paused = false;

          entry.clips[clipName] = {
            duration: round(duration),
            frameCount,
            frames,
          };
        }

        result.characters[charId] = entry;
      }

      return result;
    });

    writeFileSync(OUTPUT_PATH, asJsModule(data));
    console.log(`Wrote authoritative tracks to ${OUTPUT_PATH}`);
  } finally {
    await browser?.close();
    killProcessTree(app.proc);
    await delay(150);
    const stderr = app.getStderr().trim();
    if (stderr) {
      console.error(stderr);
    }
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
